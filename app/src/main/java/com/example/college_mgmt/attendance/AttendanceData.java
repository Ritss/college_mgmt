package com.example.college_mgmt.attendance;

public class AttendanceData {
	
	private String attendance_id;
	private String staff_id;
	private String class_id;
	private String division_id;
	private String subject_id;
	private String date;
	private String time;
	/**
	 * @return the attendance_id
	 */
	public String getAttendance_id() {
		return attendance_id;
	}
	/**
	 * @param attendance_id the attendance_id to set
	 */
	public void setAttendance_id(String attendance_id) {
		this.attendance_id = attendance_id;
	}
	/**
	 * @return the staff_id
	 */
	public String getStaff_id() {
		return staff_id;
	}
	/**
	 * @param staff_id the staff_id to set
	 */
	public void setStaff_id(String staff_id) {
		this.staff_id = staff_id;
	}
	/**
	 * @return the class_id
	 */
	public String getClass_id() {
		return class_id;
	}
	/**
	 * @param class_id the class_id to set
	 */
	public void setClass_id(String class_id) {
		this.class_id = class_id;
	}
	/**
	 * @return the division_id
	 */
	public String getDivision_id() {
		return division_id;
	}
	/**
	 * @param division_id the division_id to set
	 */
	public void setDivision_id(String division_id) {
		this.division_id = division_id;
	}
	/**
	 * @return the subject_id
	 */
	public String getSubject_id() {
		return subject_id;
	}
	/**
	 * @param subject_id the subject_id to set
	 */
	public void setSubject_id(String subject_id) {
		this.subject_id = subject_id;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the time
	 */
	public String getTime() {
		return time;
	}
	/**
	 * @param time the time to set
	 */
	public void setTime(String time) {
		this.time = time;
	}
	
	

}
